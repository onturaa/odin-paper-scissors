const OPTIONS = 3;

function getComputerChoice() {
    let number = Math.floor(Math.random() * OPTIONS);
    if (number === 0) {
        return "rock";
    }
    else if (number === 1) {
        return "paper";
    }
    else if (number === 2) {
        return "scissors";
    }
    else {
        return "error";
    }
}

